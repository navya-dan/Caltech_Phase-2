package com.caltech.pojo;

public class Batch {
  private int bid;
  private String bname;
  private String binstructor;
  private String bdate;
  private String btime;
public int getBid() {
	return bid;
}
public void setBid(int bid) {
	this.bid = bid;
}
public String getBname() {
	return bname;
}
public void setBname(String bname) {
	this.bname = bname;
}
public String getBinstructor() {
	return binstructor;
}
public void setBinstructor(String binstructor) {
	this.binstructor = binstructor;
}
public String getDate() {
	return bdate;
}
public void setDate(String date) {
	this.bdate = date;
}
public String getTime() {
	return btime;
}
public void setTime(String time) {
	this.btime = time;
}
  
}
